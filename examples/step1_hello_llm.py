"""
╔══════════════════════════════════════════════════════════════╗
║  第 1 步：你好，大模型！—— 最简单的 LLM 对话                  ║
╚══════════════════════════════════════════════════════════════╝

学习目标
--------
理解 LLM（大语言模型）是什么，以及如何用最简单的方式"和它说话"。

核心概念
--------
LLM = Large Language Model（大语言模型）
  就像一个知识渊博的"聊天机器人"，你发一条消息，它回一条消息。

消息格式
--------
每条消息有两个字段：
  role    = "user"（你）或 "assistant"（AI）
  content = 消息内容文字

运行方法
--------
  cd /path/to/ai-life-agent
  uv run python examples/step1_hello_llm.py
"""

# ============================================================
# 导入 MiniMax 客户端（已封装好的 LLM 通信模块）
# ============================================================
from ai_life_agent.llm.client import MiniMaxClient


def main():
    # ----------------------------------------------------------
    # 1. 创建 LLM 客户端
    #    MiniMaxClient 会自动读取 .env 文件中的 MINIMAX_API_KEY
    # ----------------------------------------------------------
    client = MiniMaxClient()

    print("=" * 60)
    print("  第 1 步：和大模型直接对话")
    print("=" * 60)

    # ----------------------------------------------------------
    # 2. 最简单的对话 —— 单条消息
    #
    #    messages 是一个列表，每项代表一条消息：
    #      - role="user"      表示"你"说的话
    #      - content=...      消息内容
    # ----------------------------------------------------------
    print("\n【示例 1】单轮问答\n")

    response = client.chat(
        messages=[
            {"role": "user", "content": "你好！请用一句话介绍你自己。"}
        ]
    )

    print("你   : 你好！请用一句话介绍你自己。")
    print(f"AI  : {response.text}")

    # ----------------------------------------------------------
    # 3. 多轮对话 —— 把历史消息也带上
    #
    #    关键：每次请求都要把「之前所有的对话记录」一起发给 LLM，
    #    否则 LLM 不知道之前聊过什么（它没有记忆！）
    # ----------------------------------------------------------
    print("\n【示例 2】多轮对话（LLM 记住上文）\n")

    # 构建对话历史
    conversation = [
        {"role": "user",      "content": "我叫小明，我喜欢 Python。"},
        {"role": "assistant", "content": "你好，小明！Python 是一门很棒的语言。"},
        {"role": "user",      "content": "你还记得我叫什么名字吗？"},
    ]

    response2 = client.chat(messages=conversation)

    for msg in conversation:
        who = "你   " if msg["role"] == "user" else "AI  "
        print(f"{who}: {msg['content']}")
    print(f"AI  : {response2.text}")

    # ----------------------------------------------------------
    # 4. 用 system 消息给 AI 设定"人设"
    #
    #    system = 系统提示词，在对话开始之前告诉 AI"你是谁、你该做什么"
    # ----------------------------------------------------------
    print("\n【示例 3】用 system 设定 AI 角色\n")

    response3 = client.chat(
        messages=[{"role": "user", "content": "你好，请介绍一下你自己"}],
        system="你是一个古代中国的诗人，说话必须用文言文，并且每次回答都包含一句自创的诗句。",
    )

    print("你   : 你好，请介绍一下你自己")
    print(f"AI  : {response3.text}")

    # ----------------------------------------------------------
    # 小结
    # ----------------------------------------------------------
    print("\n" + "=" * 60)
    print("  小结")
    print("=" * 60)
    print("""
  ✅ LLM 就是一个"问答机器"
  ✅ 用 messages 列表控制对话历史
  ✅ 用 system 提示词控制 AI 的"人设"
  ✅ 每次发送时要带上所有历史记录（LLM 本身没有记忆）

  下一步：学习如何让 AI 调用工具 → 运行 step2_tools.py
""")


if __name__ == "__main__":
    main()
